alamat = ["Semarang", "Central Java"];
console.log(alamat.join(" - "));

// Push : nambah elemen diakhir array
alamat.push("Indonesia", "Bumi");

//Pop : hapus elemen diakhir array
alamat.pop();
console.log(alamat);

// Shif : Hapus elemen diawal array
alamat.shift()

// Unshift : tambah elemen diawal array
alamat.unshift('Masaran', 'Sragen')
console.log(alamat);
